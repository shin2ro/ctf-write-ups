def solve():
    key = [0x6e, 0x69, 99, 0x6b, 0x65, 0x6c, 0x6f, 100, 0x65, 0x6f, 0x6e]
    e = [
        0x80, 0x97, 0x85, 0xd7, 0x81, 0x98, 0x87, 0xd2, 0x87, 0xbc,
        0x9a, 0xd3, 0x96, 0xbc, 0x87, 0xd0, 0x80, 0x91, 0x9a, 0x93,
        0x97, 0xbc, 0x91, 0x80, 0xd7, 0xdc, 0x9e
    ]

    bs = [i for i in range(0x100)]

    i = 0
    for j in range(0x100):
        k = key[j % len(key)] + bs[j] + i
        u = (k >> 0x1f) >> 0x18
        i = (k + u & 0xff) - u

        bs[j], bs[i] = bs[i], bs[j]

    flag = ''

    for k in range(len(e)):
        flag += chr(e[k] ^ bs[bs[0] * 0x02])

    return flag


if __name__ == '__main__':
    print(solve())
